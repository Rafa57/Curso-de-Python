numero = input('Digite um número inteiro: ')
numero = int(numero)

def verificar_numero():
    
    if numero % 2 == 0:
        print('O número é par!')
    else:
        print('O número é ímpar!')

def verificar_inteiro():

    if numero != int:
        print("O número digitado não é inteiro")

verificar_inteiro()
verificar_numero()